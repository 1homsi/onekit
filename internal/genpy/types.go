const pyNone = "None"

func pyDefaultValue(t *onkir.Type) string {
	if t == nil {
		return pyNone
	}
	switch t.Kind {
	case onkir.KindScalar:
		switch t.Scalar {
		case onkir.ScalarString, onkir.ScalarTimestamp:
			return `""`
		case onkir.ScalarBool:
			return "False"
		case onkir.ScalarInt32, onkir.ScalarInt64, onkir.ScalarUint32, onkir.ScalarUint64:
			return "0"
		case onkir.ScalarFloat32, onkir.ScalarFloat64:
			return "0.0"
		case onkir.ScalarBytes:
			return `b""`
		default:
			return pyNone
		}
	case onkir.KindMessage, onkir.KindEnum, onkir.KindMap:
		return pyNone
	default:
		return pyNone
	}
}

// fieldPyType/fieldPyDefault let a field's @encode decorator override the
// natural Python type: only timestamp fields need this (unix_seconds/millis
// serialize as int, not the default RFC3339 str). int64/enum/bytes fields
// keep their natural Python type regardless of wire encoding, since to_dict/
// from_dict already do the string/base64/etc. conversion explicitly.
func (p *Printer) fieldPyType(f *onkir.Field) string {
	if f.Type != nil && f.Type.Kind == onkir.KindScalar && f.Type.Scalar == onkir.ScalarTimestamp {
		switch timestampEncodingValue(f) {
		case timestampEncodeUnixSeconds, timestampEncodeUnixMillis:
			return "int"
		}
	}
	return p.PyFieldType(f.Type)
}

func fieldPyDefault(f *onkir.Field) string {
	if f.Type != nil && f.Type.Kind == onkir.KindScalar && f.Type.Scalar == onkir.ScalarTimestamp {
		switch timestampEncodingValue(f) {
		case timestampEncodeUnixSeconds, timestampEncodeUnixMillis:
			return "0"
		}
	}
	return pyDefaultValue(f.Type)
}

func isBytesField(f *onkir.Field) bool {
	return f.Type != nil && f.Type.Kind == onkir.KindScalar && f.Type.Scalar == onkir.ScalarBytes && !f.Repeated
}

func bytesEncodeExpr(encoding, expr string) string {
	switch encoding {
	case bytesEncodeHex:
		return expr + ".hex()"
	case bytesEncodeBase64Raw:
		return `base64.b64encode(` + expr + `).decode("ascii").rstrip("=")`
	case bytesEncodeBase64URL:
		return `base64.urlsafe_b64encode(` + expr + `).decode("ascii")`
	case bytesEncodeBase64URLRaw:
		return `base64.urlsafe_b64encode(` + expr + `).decode("ascii").rstrip("=")`
	default:
		return `base64.b64encode(` + expr + `).decode("ascii")`
	}
}

func bytesDecodeExpr(encoding, key string) string {
	switch encoding {
	case bytesEncodeHex:
		return fmt.Sprintf("(bytes.fromhex(d[%s]) if d.get(%s) is not None else b\"\")", key, key)
	case bytesEncodeBase64Raw:
		return fmt.Sprintf(
			"(base64.b64decode(d[%s] + \"=\" * (-len(d[%s]) %% 4)) if d.get(%s) is not None else b\"\")",
			key, key, key,
		)
	case bytesEncodeBase64URL:
		return fmt.Sprintf("(base64.urlsafe_b64decode(d[%s]) if d.get(%s) is not None else b\"\")", key, key)
	case bytesEncodeBase64URLRaw:
		return fmt.Sprintf(
			"(base64.urlsafe_b64decode(d[%s] + \"=\" * (-len(d[%s]) %% 4)) if d.get(%s) is not None else b\"\")",
			key, key, key,
		)
	default:
		return fmt.Sprintf("(base64.b64decode(d[%s]) if d.get(%s) is not None else b\"\")", key, key)
	}
}

func enumToJSONMapName(e *onkir.Enum) string {
	return "_" + strings.ToUpper(e.Name) + "_TO_JSON"
}

func enumFromJSONMapName(e *onkir.Enum) string {
	return "_" + strings.ToUpper(e.Name) + "_FROM_JSON"
}

// enumToJSONMapRef/enumFromJSONMapRef qualify the enum's module-level JSON
// lookup dict with the owning module's alias when e isn't declared in the
// module currently being printed (see enumToJSONMapName/enumFromJSONMapName,
// which only return the bare, unqualified constant name).
func (p *Printer) enumToJSONMapRef(e *onkir.Enum) string {
	if p.resolver != nil {
		if ref, ok := p.resolver.ResolveEnum(e); ok {
			return ref.Alias + "." + enumToJSONMapName(e)
		}
	}
	return enumToJSONMapName(e)
}

func (p *Printer) enumFromJSONMapRef(e *onkir.Enum) string {
	if p.resolver != nil {
		if ref, ok := p.resolver.ResolveEnum(e); ok {
			return ref.Alias + "." + enumFromJSONMapName(e)
		}
	}
	return enumFromJSONMapName(e)
}

// GenerateTypes generates models.py treating every message/enum type as
// local. Use GenerateTypesWithResolver for a multi-package project where some
// referenced types live in a different generated module.
func GenerateTypes(file *onkir.File) []byte {
	return GenerateTypesWithResolver(file, nil)
}

func GenerateTypesWithResolver(file *onkir.File, resolver PackageResolver) []byte {
	p := newPrinter(resolver)
	p.P("# Code generated by onek. DO NOT EDIT.")
	p.P("from __future__ import annotations")
	p.P()
	if fileNeedsBase64Import(file) {
		p.P("import base64")
	}
	p.P("from dataclasses import dataclass, field")
	p.P("from enum import IntEnum")
	for _, ref := range collectExternalRefs(file, resolver) {
		p.P("import ", ref.ModulePath, " as ", ref.Alias)
	}
	p.P()

	for _, e := range file.Enums {
		writeEnum(p, e)
	}
	for _, m := range file.Messages {
		if m.IsError() {
			writeErrorClass(p, m)
		} else {
			writeMessage(p, m)
		}
	}

	return p.Bytes()
}

func writeEnum(p *Printer, e *onkir.Enum) {
	p.P("class ", e.Name, "(IntEnum):")
	p.Indent()
	for i, v := range e.Values {
		p.P(v.Name, " = ", strconv.Itoa(i))
	}
	p.Dedent()
	p.Blank()

	toJSON := enumToJSONMapName(e)
	p.P(toJSON, ": dict[", e.Name, ", str] = {")
	p.Indent()
	for _, v := range e.Values {
		p.P(e.Name, ".", v.Name, ": ", fmt.Sprintf("%q", v.JSONName()), ",")
	}
	p.Dedent()
	p.P("}")
	p.Blank()
	p.P(enumFromJSONMapName(e), ": dict[str, ", e.Name, "] = {v: k for k, v in ", toJSON, ".items()}")
	p.Blank()
}

func writeFieldDecl(p *Printer, f *onkir.Field) {
	if f.Oneof != nil {
		p.P(f.Name, ": dict | None = None")
		return
	}
	pyType := p.fieldPyType(f)
	switch {
	case f.Repeated:
		p.P(f.Name, ": list[", pyType, "] = field(default_factory=list)")
	case f.Type.Kind == onkir.KindMap:
		p.P(f.Name, ": ", pyType, " = field(default_factory=dict)")
	case f.Optional || f.Type.Kind == onkir.KindMessage:
		p.P(f.Name, ": ", pyType, " | None = None")
	default:
		p.P(f.Name, ": ", pyType, " = ", fieldPyDefault(f))
	}
}

func writeMessageFieldToDict(p *Printer, f *onkir.Field, key string) {
	if prefix, ok := flattenPrefix(f); ok {
		p.P("if self.", f.Name, " is not None:")
		p.Indent()
		p.P("for k, v in self.", f.Name, ".to_dict().items():")
		p.Indent()
		p.P("d[", fmt.Sprintf("%q", prefix), " + k] = v")
		p.Dedent()
		p.Dedent()
		return
	}

	if behavior := emptyBehaviorValue(f); behavior != "" && behavior != emptyBehaviorPreserve {
		tmpVar := "_" + f.Name + "_d"
		p.P("if self.", f.Name, " is not None:")
		p.Indent()
		p.P(tmpVar, " = self.", f.Name, ".to_dict()")
		p.P("if ", tmpVar, " == {}:")
		p.Indent()
		if behavior == emptyBehaviorNull {
			p.P("d[", key, "] = None")
		} else {
			p.P("pass")
		}
		p.Dedent()
		p.P("else:")
		p.Indent()
		p.P("d[", key, "] = ", tmpVar)
		p.Dedent()
		p.Dedent()
		if behavior == emptyBehaviorNull {
			p.P("else:")
			p.Indent()
			p.P("d[", key, "] = None")
			p.Dedent()
		}
		return
	}

	p.P("if self.", f.Name, " is not None:")
	p.Indent()
	p.P("d[", key, "] = self.", f.Name, ".to_dict()")
	p.Dedent()
}

func writeRepeatedFieldToDict(p *Printer, f *onkir.Field, key string) {
	p.P("if self.", f.Name, ":")
	p.Indent()
	switch {
	case f.Type.Kind == onkir.KindMessage:
		p.P("d[", key, "] = [v.to_dict() for v in self.", f.Name, "]")
	case needsInt64StringEncoding(f):
		p.P("d[", key, "] = [str(v) for v in self.", f.Name, "]")
	default:
		p.P("d[", key, "] = list(self.", f.Name, ")")
	}
	p.Dedent()
}

func writeMapFieldToDict(p *Printer, f *onkir.Field, key string) {
	p.P("if self.", f.Name, ":")
	p.Indent()
	if f.Type.MapValue.Kind == onkir.KindMessage {
		p.P("d[", key, "] = {k: v.to_dict() for k, v in self.", f.Name, ".items()}")
	} else {
		p.P("d[", key, "] = dict(self.", f.Name, ")")
	}
	p.Dedent()
}

func writeToDictField(p *Printer, f *onkir.Field) {
	if f.Oneof != nil {
		p.P("if self.", f.Name, " is not None:")
		p.Indent()
		p.P("d[", fmt.Sprintf("%q", f.Name), "] = dict(self.", f.Name, ")")
		p.Dedent()
		return
	}

	key := fmt.Sprintf("%q", f.Name)

	switch {
	case f.Repeated:
		writeRepeatedFieldToDict(p, f, key)
	case f.Type.Kind == onkir.KindMap:
		writeMapFieldToDict(p, f, key)
	case f.Type.Kind == onkir.KindMessage:
		writeMessageFieldToDict(p, f, key)
	case f.Type.Kind == onkir.KindEnum:
		p.P("if self.", f.Name, " is not None:")
		p.Indent()
		if needsEnumNumberEncoding(f) {
			p.P("d[", key, "] = int(self.", f.Name, ")")
		} else {
			p.P("d[", key, "] = ", p.enumToJSONMapRef(f.Type.Enum), "[self.", f.Name, "]")
		}
		p.Dedent()
	case isBytesField(f):
		p.P("if self.", f.Name, ":")
		p.Indent()
		p.P("d[", key, "] = ", bytesEncodeExpr(bytesEncodingValue(f), "self."+f.Name))
		p.Dedent()
	case needsInt64StringEncoding(f):
		p.P("if self.", f.Name, ":")
		p.Indent()
		p.P("d[", key, "] = str(self.", f.Name, ")")
		p.Dedent()
	case f.Optional:
		p.P("if self.", f.Name, " is not None:")
		p.Indent()
		p.P("d[", key, "] = self.", f.Name)
		p.Dedent()
	default:
		// Matches gengo/gents' default `omitempty` wire behavior: a
		// required scalar field at its zero value is indistinguishable
		// from "unset" on the wire, so it's simply omitted.
		p.P("if self.", f.Name, ":")
		p.Indent()
		p.P("d[", key, "] = self.", f.Name)
		p.Dedent()
	}
}

func (p *Printer) messageFieldFromDictExpr(f *onkir.Field, key string) string {
	if prefix, ok := flattenPrefix(f); ok {
		childName := p.MessageTypeName(f.Type.Message)
		prefixLit := fmt.Sprintf("%q", prefix)
		expr := "(%s.from_dict({k[len(%s):]: v for k, v in d.items() if k.startswith(%s)})" +
			" if any(k.startswith(%s) for k in d.keys()) else None)"
		return fmt.Sprintf(expr, childName, prefixLit, prefixLit, prefixLit)
	}
	childName := p.MessageTypeName(f.Type.Message)
	return fmt.Sprintf("(%s.from_dict(d[%s]) if d.get(%s) is not None else None)", childName, key, key)
}

func (p *Printer) writeFromDictField(f *onkir.Field) string {
	key := fmt.Sprintf("%q", f.Name)
	if f.Oneof != nil {
		return fmt.Sprintf("d.get(%s)", key)
	}
	switch {
	case f.Repeated:
		if f.Type.Kind == onkir.KindMessage {
			childName := p.MessageTypeName(f.Type.Message)
			return fmt.Sprintf("[%s.from_dict(v) for v in d.get(%s, [])]", childName, key)
		}
		if needsInt64StringEncoding(f) {
			return fmt.Sprintf("[int(v) for v in d.get(%s, [])]", key)
		}
		return fmt.Sprintf("list(d.get(%s, []))", key)
	case f.Type.Kind == onkir.KindMap:
		if f.Type.MapValue.Kind == onkir.KindMessage {
			valueName := p.MessageTypeName(f.Type.MapValue.Message)
			return fmt.Sprintf(
				"{k: %s.from_dict(v) for k, v in d.get(%s, {}).items()}",
				valueName, key,
			)
		}
		return fmt.Sprintf("dict(d.get(%s, {}))", key)
	case f.Type.Kind == onkir.KindMessage:
		return p.messageFieldFromDictExpr(f, key)
	case f.Type.Kind == onkir.KindEnum:
		if needsEnumNumberEncoding(f) {
			enumName := p.EnumTypeName(f.Type.Enum)
			return fmt.Sprintf("(%s(d[%s]) if d.get(%s) is not None else None)", enumName, key, key)
		}
		enumRef := p.enumFromJSONMapRef(f.Type.Enum)
		return fmt.Sprintf("(%s[d[%s]] if d.get(%s) is not None else None)", enumRef, key, key)
	case isBytesField(f):
		return bytesDecodeExpr(bytesEncodingValue(f), key)
	case needsInt64StringEncoding(f):
		return fmt.Sprintf("int(d.get(%s, 0))", key)
	case f.Optional:
		return fmt.Sprintf("d.get(%s)", key)
	default:
		return fmt.Sprintf("d.get(%s, %s)", key, fieldPyDefault(f))
	}
}

// rootUnwrapToExpr/rootUnwrapFromExpr build the to_dict/from_dict bodies for a
// message whose entire wire representation IS its single @unwrap field's
// value (a bare list/dict), not an object wrapping that field.
func rootUnwrapToExpr(f *onkir.Field) string {
	switch {
	case f.Repeated && f.Type.Kind == onkir.KindMessage:
		return fmt.Sprintf("[v.to_dict() for v in self.%s]", f.Name)
	case f.Repeated:
		return fmt.Sprintf("list(self.%s)", f.Name)
	case f.Type.Kind == onkir.KindMap:
		if f.Type.MapValue.Kind == onkir.KindMessage {
			return fmt.Sprintf("{k: v.to_dict() for k, v in self.%s.items()}", f.Name)
		}
		return fmt.Sprintf("dict(self.%s)", f.Name)
	case f.Type.Kind == onkir.KindMessage:
		return fmt.Sprintf("(self.%s.to_dict() if self.%s is not None else None)", f.Name, f.Name)
	default:
		return "self." + f.Name
	}
}

func (p *Printer) rootUnwrapFromExpr(f *onkir.Field) string {
	switch {
	case f.Repeated && f.Type.Kind == onkir.KindMessage:
		return fmt.Sprintf("[%s.from_dict(v) for v in d]", p.MessageTypeName(f.Type.Message))
	case f.Repeated:
		return "list(d)"
	case f.Type.Kind == onkir.KindMap:
		if f.Type.MapValue.Kind == onkir.KindMessage {
			valueName := p.MessageTypeName(f.Type.MapValue.Message)
			return fmt.Sprintf("{k: %s.from_dict(v) for k, v in d.items()}", valueName)
		}
		return "dict(d)"
	case f.Type.Kind == onkir.KindMessage:
		return fmt.Sprintf("(%s.from_dict(d) if d is not None else None)", p.MessageTypeName(f.Type.Message))
	default:
		return "d"
	}
}

func writeRootUnwrapClass(p *Printer, m *onkir.Message, f *onkir.Field) {
	p.P("@dataclass")
	p.P("class ", m.Name, ":")
	p.Indent()
	writeFieldDecl(p, f)
	p.Blank()

	p.P("def to_dict(self) -> object:")
	p.Indent()
	p.P("return ", rootUnwrapToExpr(f))
	p.Dedent()
	p.Blank()

	p.P("@staticmethod")
	p.P("def from_dict(d: object) -> ", fmt.Sprintf("%q", m.Name), ":")
	p.Indent()
	p.P("return ", m.Name, "(", f.Name, "=", p.rootUnwrapFromExpr(f), ")")
	p.Dedent()
	p.Blank()
}

func writeMessage(p *Printer, m *onkir.Message) {
	if field := rootUnwrapField(m); field != nil {
		writeRootUnwrapClass(p, m, field)
		for _, nested := range m.Nested {
			if nested.IsError() {
				writeErrorClass(p, nested)
			} else {
				writeMessage(p, nested)
			}
		}
		for _, nested := range m.NestedEnums {
			writeEnum(p, nested)
		}
		return
	}

	p.P("@dataclass")
	p.P("class ", m.Name, ":")
	p.Indent()
	if len(m.Fields) == 0 {
		p.P("pass")
	}
	for _, f := range m.Fields {
		writeFieldDecl(p, f)
	}
	p.Blank()

	p.P("def to_dict(self) -> dict:")
	p.Indent()
	p.P("d: dict = {}")
	for _, f := range m.Fields {
		writeToDictField(p, f)
	}
	p.P("return d")
	p.Dedent()
	p.Blank()

	p.P("@staticmethod")
	p.P("def from_dict(d: dict) -> ", fmt.Sprintf("%q", m.Name), ":")
	p.Indent()
	p.P("return ", m.Name, "(")
	p.Indent()
	for _, f := range m.Fields {
		p.P(f.Name, "=", p.writeFromDictField(f), ",")
	}
	p.Dedent()
	p.P(")")
	p.Dedent()
	p.Dedent()
	p.Blank()

	for _, nested := range m.Nested {
		if nested.IsError() {
			writeErrorClass(p, nested)
		} else {
			writeMessage(p, nested)
		}
	}
	for _, nested := range m.NestedEnums {
		writeEnum(p, nested)
	}
}

func writeErrorClass(p *Printer, m *onkir.Message) {
	p.P("class ", m.Name, "(Exception):")
	p.Indent()
	p.P("def __init__(self", p.errorInitArgs(m), ") -> None:")
	p.Indent()
	for _, f := range m.Fields {
		p.P("self.", f.Name, " = ", f.Name)
	}
	parts := make([]string, len(m.Fields))
	for i, f := range m.Fields {
		parts[i] = fmt.Sprintf("%s={self.%s!r}", f.Name, f.Name)
	}
	p.P(fmt.Sprintf("super().__init__(f%q)", m.Name+" "+strings.Join(parts, " ")))
	p.Dedent()
	p.Blank()

	p.P("def to_dict(self) -> dict:")
	p.Indent()
	p.P("d: dict = {}")
	for _, f := range m.Fields {
		writeToDictField(p, f)
	}
	p.P("return d")
	p.Dedent()
	p.Blank()

	p.P("@staticmethod")
	p.P("def from_dict(d: dict) -> ", fmt.Sprintf("%q", m.Name), ":")
	p.Indent()
	p.P("return ", m.Name, "(")
	p.Indent()
	for _, f := range m.Fields {
		p.P(f.Name, "=", p.writeFromDictField(f), ",")
	}
	p.Dedent()
	p.P(")")
	p.Dedent()
	p.Dedent()
	p.Blank()
}

func (p *Printer) errorInitArgs(m *onkir.Message) string {
	var sb strings.Builder
	for _, f := range m.Fields {
		sb.WriteString(", ")
		sb.WriteString(f.Name)
		sb.WriteString(": ")
		pyType := p.fieldPyType(f)
		if f.Repeated {
			pyType = "list[" + pyType + "]"
		}
		sb.WriteString(pyType)
		sb.WriteString(" = ")
		sb.WriteString(fieldPyDefault(f))
	}
	return sb.String()
}
